create table if not exists person (
    id bigint generated by default as identity primary key,
    username varchar(20) unique not null,
    password varchar not null
);

create table if not exists languages (
    id bigint generated by default as identity primary key,
    title varchar unique not null
);

create table if not exists person_words (
    id bigint generated by default as identity primary key,
    title varchar unique not null,
    word_translation varchar not null,
    person_id bigint references person(id) not null,
    language_id bigint references languages(id) not null,
    local_date_time timestamp without time zone default current_timestamp not null
);
